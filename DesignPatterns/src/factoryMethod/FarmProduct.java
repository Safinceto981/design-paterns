package factoryMethod;

public abstract class FarmProduct {

	private double price;

	/**
	 * @return the price
	 */
	public double getPrice() {
		return price;
	}

	/**
	 * @param price the price to set
	 */
	public void setPrice(double price) {
		this.price = price;
	}
	public abstract void prepare();
}
